# Comparison of two nums if one greater than other.

number_given = 34
number_to_compare = 80

if number_given > number_to_compare:
    print(f"{number_given} is greater than {number_to_compare}")
elif number_given < number_to_compare:
    print(f"{number_given} is less than {number_to_compare}")
else:
    print(f"{number_given} is equal to {number_to_compare}")
